create or replace view RETAIL_DW.MART.VW_EXECUTIVE_KPI(
	TOTAL_ORDERS,
	TOTAL_CUSTOMERS,
	TOTAL_REVENUE,
	AVG_ORDER_VALUE
) as
SELECT
    COUNT(DISTINCT ORDERID) AS TOTAL_ORDERS,
    COUNT(DISTINCT CUSTOMERID) AS TOTAL_CUSTOMERS,
    SUM(LINE_AMOUNT) AS TOTAL_REVENUE,
    AVG(LINE_AMOUNT) AS AVG_ORDER_VALUE
FROM MART.FACT_SALES;
create or replace view RETAIL_DW.MART.VW_PRODUCT_PERFORMANCE(
	PRODUCTID,
	TOTAL_REVENUE,
	TOTAL_QTY
) as
SELECT
    PRODUCTID,
    SUM(LINE_AMOUNT) AS TOTAL_REVENUE,
    SUM(QTY) AS TOTAL_QTY
FROM MART.FACT_SALES
GROUP BY PRODUCTID;
create or replace view RETAIL_DW.MART.VW_SALES_ANALYTICS(
	ORDERID,
	ORDERDATE,
	MONTH_NAME,
	CUSTOMER_NAME,
	PRODUCT_NAME,
	CATEGORY,
	QTY,
	PRICE,
	LINE_AMOUNT
) as
SELECT
    F.ORDERID,
    F.ORDERDATE,
    D.MONTH_NAME,
    C.CUSTOMER_NAME,
    P.PRODUCT_NAME,
    P.CATEGORY,
    F.QTY,
    F.PRICE,
    F.LINE_AMOUNT
FROM MART.FACT_SALES F
JOIN MART.DIM_CUSTOMER C
    ON F.CUSTOMERID = C.CUSTOMER_ID
JOIN MART.DIM_PRODUCT P
    ON F.PRODUCTID = P.PRODUCTID
JOIN MART.DIM_DATE D
    ON F.ORDERDATE = D.DATE_KEY;
