CREATE OR REPLACE PROCEDURE RETAIL_DW.RAW.SP_REFRESH_ORDER_ITEMS()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN

    MERGE INTO RETAIL_DW.STAGING.STG_ORDER_ITEMS T
    USING (
        SELECT
            ITEMID,
            ORDERID,
            PRODUCTID,
            QTY,
            PRICE,
            METADATA$ACTION
        FROM RETAIL_DW.RAW.ORDER_ITEMS_STREAM
    ) S
    ON T.ITEMID = S.ITEMID

    WHEN MATCHED
         AND S.METADATA$ACTION = ''DELETE''
    THEN DELETE

    WHEN MATCHED
         AND S.METADATA$ACTION <> ''DELETE''
    THEN
        UPDATE SET
            ORDERID   = S.ORDERID,
            PRODUCTID = S.PRODUCTID,
            QTY       = S.QTY,
            PRICE     = S.PRICE

    WHEN NOT MATCHED
         AND S.METADATA$ACTION <> ''DELETE''
    THEN
        INSERT (
            ITEMID,
            ORDERID,
            PRODUCTID,
            QTY,
            PRICE
        )
        VALUES (
            S.ITEMID,
            S.ORDERID,
            S.PRODUCTID,
            S.QTY,
            S.PRICE
        );

    INSERT INTO RETAIL_DW.MART.ETL_AUDIT_LOG
    (
        PIPELINE_NAME,
        RUN_TIME,
        RECORD_COUNT,
        STATUS
    )
    SELECT
        ''ORDER_ITEMS_REFRESH'',
        CURRENT_TIMESTAMP(),
        COUNT(*),
        ''SUCCESS''
    FROM RETAIL_DW.RAW.ORDER_ITEMS_STREAM;

    RETURN ''SUCCESS'';

END;
';
