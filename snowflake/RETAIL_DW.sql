CREATE OR REPLACE PROCEDURE RETAIL_DW.STAGING.SP_BUILD_FACT_SALES()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN

    MERGE INTO RETAIL_DW.MART.FACT_SALES T
    USING
    (
        SELECT
            O.ORDERID,
            O.ORDERDATE,
            O.CUSTOMERID,
            I.PRODUCTID,
            I.QTY,
            I.PRICE,
            (I.QTY * I.PRICE) AS TOTAL_AMOUNT
        FROM RETAIL_DW.STAGING.STG_ORDERS O
        INNER JOIN RETAIL_DW.STAGING.STG_ORDER_ITEMS I
            ON O.ORDERID = I.ORDERID
    ) S
    ON T.ORDER_ID = S.ORDERID
    AND T.PRODUCT_ID = S.PRODUCTID

    WHEN MATCHED THEN
        UPDATE SET
            ORDER_DATE   = S.ORDERDATE,
            CUSTOMER_ID  = S.CUSTOMERID,
            QUANTITY     = S.QTY,
            UNIT_PRICE   = S.PRICE,
            TOTAL_AMOUNT = S.TOTAL_AMOUNT

    WHEN NOT MATCHED THEN
        INSERT
        (
            ORDER_ID,
            ORDER_DATE,
            CUSTOMER_ID,
            PRODUCT_ID,
            QUANTITY,
            UNIT_PRICE,
            TOTAL_AMOUNT
        )
        VALUES
        (
            S.ORDERID,
            S.ORDERDATE,
            S.CUSTOMERID,
            S.PRODUCTID,
            S.QTY,
            S.PRICE,
            S.TOTAL_AMOUNT
        );

    INSERT INTO RETAIL_DW.MART.ETL_AUDIT_LOG
    (
        PIPELINE_NAME,
        RUN_TIME,
        RECORD_COUNT,
        STATUS
    )
    VALUES
    (
        ''SP_BUILD_FACT_SALES'',
        CURRENT_TIMESTAMP(),
        NULL,
        ''SUCCESS''
    );

    RETURN ''SUCCESS'';

EXCEPTION
    WHEN OTHER THEN

        INSERT INTO RETAIL_DW.MART.ETL_AUDIT_LOG
        (
            PIPELINE_NAME,
            RUN_TIME,
            RECORD_COUNT,
            STATUS
        )
        VALUES
        (
            ''SP_BUILD_FACT_SALES'',
            CURRENT_TIMESTAMP(),
            NULL,
            ''FAILED''
        );

        RETURN ''FAILED: '' || SQLERRM;

END;
