CREATE OR REPLACE PROCEDURE RETAIL_DW.RAW.SP_REFRESH_CUSTOMER()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN

MERGE INTO STAGING.STG_CUSTOMER T
USING (
    SELECT
        CUSTOMER_ID,
        CUSTOMERNAME,
        CITY,
        PHONE,
        STATUS,
        CREATEDDATE
    FROM CUSTOMER_STREAM
) S
ON T.CUSTOMER_ID = S.CUSTOMER_ID

WHEN MATCHED THEN
UPDATE SET
    CUSTOMER_NAME = S.CUSTOMERNAME,
    CITY = S.CITY,
    PHONE = S.PHONE,
    STATUS = S.STATUS

WHEN NOT MATCHED THEN
INSERT (
    CUSTOMER_ID,
    CUSTOMER_NAME,
    CITY,
    PHONE,
    STATUS,
    CREATEDDATE
)
VALUES (
    S.CUSTOMER_ID,
    S.CUSTOMERNAME,
    S.CITY,
    S.PHONE,
    S.STATUS,
    S.CREATEDDATE
);

INSERT INTO MART.ETL_AUDIT_LOG
SELECT
    ''CUSTOMER_REFRESH'',
    CURRENT_TIMESTAMP(),
    COUNT(*),
    ''SUCCESS''
FROM CUSTOMER_STREAM;

RETURN ''SUCCESS'';

END;
';
